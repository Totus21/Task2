public class Number {
    private double enteredNumber;
    private int numberCapacity;

    public void setEnteredNumber(double localEnteredNumber)
    {
        enteredNumber=localEnteredNumber;
    }
    public double getEnteredNumber()
    {
      return enteredNumber;
    }

    public void setNumberCapacity(int localNumberCapacity)
    {
        numberCapacity=localNumberCapacity;
    }
    public int getNumberCapacity()
    {
        return numberCapacity;
    }



}
